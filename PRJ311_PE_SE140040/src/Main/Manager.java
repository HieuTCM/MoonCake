    private boolean validData() {
        String s = "";
        if (addNew == true){
            s = this.txtCode.getText().trim().toUpperCase();
            this.txtCode.setText(s);
            if (!s.matches("^\\d{3,5}$")){
                JOptionPane.showMessageDialog(this, "Code format: 3-5 digit");
                return false;
            }
            int code = Integer.parseInt(txtCode.getText());
            if(code<100 || code>99999){
                JOptionPane.showMessageDialog(this, "Minimum code: 100");
                return false;
            }
            for ( int i = 0; i< model.getRowCount() ;i++) {
                if(code == model.getPro().get(i).getID()){
                    JOptionPane.showMessageDialog(this, "Code duplicated");
                    txtCode.requestFocus();
                    return false;
                }
            }
        }     
        
        s = this.txtName.getText().trim(); 
        if (s.length()==0){
            JOptionPane.showMessageDialog(this, "Name must not be empty");
            return false;
        }
        s = this.txtPrice.getText().trim();
        if(s.isEmpty()){
            JOptionPane.showMessageDialog(this, "Price must not be empty");
            return false;
        }
        if(!s.matches("^((\\d+(\\.\\d*)?)|(\\.\\d+))$")){
            JOptionPane.showMessageDialog(this, "Price must be number");
            return false;
        }       
        float price = Float.parseFloat(txtPrice.getText());
        if(price<0){
            JOptionPane.showMessageDialog(this, "Price must more than 0");
            return false;
        }
        
        return true;
    }

