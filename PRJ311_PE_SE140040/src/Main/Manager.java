    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(!validData())    return;
        int code = Integer.parseInt(txtCode.getText());
        String name = txtName.getText();
        float price = Float.parseFloat(txtPrice.getText());
        Product product = new Product(code, name, price);
        String sql = "";
        String msg = "";
        if(addNew){
            sql = "insert into Product values ('"+code+"', '"+name+"', '"+price+"')";
            msg = "An product has been ADDED";
        }
        else {
            sql = "update Product set Name = '"+name+"', Price = '"+price+"' where ID = '"+code+"'";
            msg = "An product has been UPDATED";
        }
        try {
            int n = access.executeUpdate(sql);
            if(n>0){
                JOptionPane.showMessageDialog(this, msg);
                if(!addNew){
                    int pos = tblProduct.getSelectedRow();
                    model.getPro().set(pos, product);
                }
                else    model.getPro().add(product);
                tblProduct.updateUI();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        addNew = false;
    }//GEN-LAST:event_btnSaveActionPerformed

