    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        int pos = tblProduct.getSelectedRow();
//        String code = txtCode.getText();
        int code = Integer.parseInt(txtCode.getText());
        String sql = "Delete from Product where ID = '" + code+ "'";
        int choice = JOptionPane.showConfirmDialog(null, "Do you want to delete "+code,"Confirm...",JOptionPane.YES_NO_OPTION);
        if(choice == 0){
            String msg = "The product: " + code + " has been deleted from DB!";
            try {
                int n = access.executeUpdate(sql);
                if (n > 0)
                {
                    JOptionPane.showMessageDialog(this, msg);
                    model.getPro().removeElementAt(pos);
                    tblProduct.updateUI();
                }
                addNew = true;
                txtCode.setText("");
                txtCode.setEditable(true);
                txtName.setText("");
                txtPrice.setText("");
            }
            catch (Exception e)
            {
                JOptionPane.showMessageDialog(this, e);
            }
        }       
        else{
            JOptionPane.showMessageDialog(this, "Delete canceling...");
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

